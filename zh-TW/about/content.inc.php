    <h1 id="page-title">關於我們</h1>

    <div class="intro">
        <p><strong>我們是個全球社群</strong>
            自 1998 年開始 <a href="http://www.mozilla.org/community">人們一起努力工作</a> 以打造更好的網際網路。<p>
      
        <p><strong>我們是個非營利組織</strong>
            致力於促進網路的 <a href="http://www.mozilla.org/about/mission.html">開放、創新，以及機會</a>。</p>
      
        <p><strong>我們也是個開放原始碼計畫</strong>
            我們的 <a href="http://www.mozilla.org/projects">軟體</a> 也被用來打造網路上一些最棒的應用程式。</p>
    </div>

    <p>我們相信，作為我們的時代當中最重要的社交與技術發展工具，網際網路是一個應該要被持續改進並保護的公共資源。</p>

    <p><a href="http://www.mozilla.org/foundation">Mozilla 基金會</a> 是一個致力於這些目標的非營利性組織，但 Mozilla 背後最真實的動力來源是全世界的人們採取行動，把網路打造成他們所想要的樣子。<a href="http://www.mozilla.org/contribute">您也可以加入幫忙</a>!</p>
</div>
