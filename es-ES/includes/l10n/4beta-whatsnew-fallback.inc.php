       <h2>Dinos qué te parece</h2>
       <p>Como usuario beta, eres parte del equipo que realiza Firefox&nbsp;4. Puedes decirnos si algo te gusta o disgusta al hacer clic en el botón <img src="<?=$screencap;?>" alt="Captura del botón de Opinión" /><em>Opinión</em>. También se te pedirá que participes en estudios de usuario anónimos, o que contestes una encuesta ocasionalmente.</p>
    </div>

    <div id="column-2">
       <h5>¿Cómo funcionan los estudios de usuarios?</h5>
       <p>Los estudios de usuarios obtienen estadísticas anónimas sobre cómo se usa Firefox, pero nunca sobre lo que estés viendo o haciendo. Tú tienes el control, y ninguna información se envía a los servidores de Test Pilot sin tu permiso. Estos datos serán usados como ayuda en el diseño de Firefox&nbsp;4.</p>
    </div>

    <div id="column-3">
       <h5>¿Qué pasa si no quieres participar?</h5>
       <p>Se te avisará antes de que un estudio comience a obtener información, y nuevamente antes de que esa información sea enviada a Mozilla. Puedes elegir no participar en cualquier estudio o descartar todos los estudios de usuario al hacer clic en el botón de <em>Opinión</em> para administrar tu configuración.</p>
    </div>

