    <h1 id="page-title">Quiénes somos</h1>

    <div class="intro">
       <p><strong>Somos una comunidad mundial</strong> de <a href="http://www.mozilla.org/community">personas trabajando juntas</a> desde 1998 para crear un mejor Internet.</p>

       <p><strong>Somos una organización sin ánimo de lucro</strong> dedicada a promover <a href="http://www.mozilla.org/about/mission.html">la apertura, innovación y oportunidad</a> en la red.</p>

       <p><strong>Y somos un proyecto de software libre y código abierto</strong> cuyo <a href="http://www.mozilla.org/projects">software</a> ha sido usado para crear algunas de las mejores aplicaciones de Internet.</p>
    </div>

    <p>Creemos que, como el desarrollo más significativo tanto social como tecnológico de nuestro tiempo, Internet es un recurso público que debe ser mejorado y protegido.</p>

    <p><a href="http://www.mozilla.org/foundation">Mozilla Foundation</a> es una organización sin ánimo de lucro dedicada a estos objetivos, pero la fuerza real detrás de Mozilla está en las personas que alrededor del mundo trabajan para crear la web que quieren. ¡<a href="http://www.mozilla.org/contribute">Y tú puedes ayudar</a>!</p>
</div>
