        <h2>Sagen Sie uns, was Sie denken</h2>
        <p>Als Beta-Tester sind Sie Teil des Teams, das Firefox&nbsp;4 voranbringt. Sagen Sie uns, wenn Sie etwas mögen oder nicht mögen, indem Sie auf die Schaltfläche <img src="<?=$screencap;?>" alt="Screenshot der Schaltfläche Feedback" /><em>Feedback</em> klicken. Sie werden auch gebeten, an anonymen Anwender-Studien teilzunehmen oder nebenher die Umfrage auszufüllen.</p>
    </div>

    <div id="column-2">
        <h5>Wie funktionieren die Anwender-Studien?</h5>
        <p>Anwender-Studien sammeln anonyme Statistiken darüber, wie Sie Firefox nutzen &#8211; aber nie darüber, was Sie im Internet sehen oder tun. Sie haben die Kontrolle und ohne Ihre Erlaubnis werden keine Daten an die Server von Mozillas Add-on Test Pilot gesendet. Diese Daten werden verwendet, um uns dabei zu helfen, Firefox&nbsp;4 zu gestalten.</p>
    </div>

    <div id="column-3">
        <h5>Was ist, wenn ich nicht teilnehmen möchte?</h5>
        <p>Sie werden informiert, bevor irgendwelche Anwender-Studien beginnen, Daten zu sammeln, und bevor irgendwelche Informationen an Mozilla gesendet werden. Sie können sich von einer Studie abmelden oder alle Studien deaktivieren, indem Sie auf die Schaltfläche <em>Feedback</em> klicken und Ihre Einstellungen bearbeiten.</p>
    </div>

