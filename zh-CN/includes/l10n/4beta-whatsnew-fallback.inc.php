        <h2>告诉我们你的看法</h2>
        <p>作为测试用户，你已经成为 Firefox&nbsp;4 团队的一员，当你觉得喜欢或者不喜欢某一特性的时候，点击 <img src="<?=$screencap;?>" alt="Feedback button screenshot" /><em>Feedback</em> 按钮告诉我们，你还会被邀请参加用户调研，还会不定期填写调查问卷。</p>
    </div>

    <div id="column-2">
        <h5>用户调研是如何进行的？</h5>
        <p>用户调研功能会匿名收集 Firefox 的使用情况，但是绝对不会记录浏览的信息和用户进行的操作。一切都在你的控制之下，不经过你的允许，不会向 Mozilla 测试服务器发送任何数据。这些信息将帮助设计 Firefox&nbsp;4 。</p>
    </div>

    <div id="column-3">
        <h5>如果我不想参加呢？</h5>
        <p>任何调研开始收集数据之前都会告知你，数据发送给 Mozilla 之前会再次告知。你可以选择不参加任一个调研，或者点击 <em>Feedback</em> 按钮在设置中关闭全部调研。</p>
    </div>

