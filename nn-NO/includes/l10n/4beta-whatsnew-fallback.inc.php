        <h2>Tell Us What You Think</h2>
        <p>As a beta tester, you’re part of the team making Firefox&nbsp;4. Tell us when you like or dislike something by clicking on the <img src="<?=$screencap;?>" alt="Feedback button screenshot" /><em>Feedback</em> button. You’ll also be asked to take part in anonymous user studies, or fill out the occasional survey.</p>
    </div>

    <div id="column-2">
        <h5>How do the user studies work?</h5>
        <p>User studies gather anonymous statistics about how you use Firefox, but never about what you’re seeing or doing online. You’re in control, and no data is sent to the Mozilla Test Pilot servers without your permission. The information will be used to help design Firefox&nbsp;4.</p>
    </div>

    <div id="column-3">
        <h5>What if I don’t want to participate?</h5>
        <p>You’ll be told before any user study begins collecting data, and again before any information is sent to Mozilla. You can choose to opt-out of any study, or to turn off all user studies by clicking the <em>Feedback</em> button to manage your settings.</p>
