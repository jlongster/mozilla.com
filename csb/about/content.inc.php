    <h1 id="page-title">Ò nas</h1>

    <div class="intro">
        <p><strong>Jesmë swiatową spòlëzną</strong>
            <a href="http://www.mozilla.org/community"> wespółrobiącëch lëdzy</a> chtërny òd 1998 rokù mają dbã ò lepszi Internet.</p>
      
        <p><strong>Nie robimë dlô dëtków</strong>
            ë wierzimë <a href="http://www.mozilla.org/about/mission.html">w òtemkłosc, jinowacjã ë równé szanse</a> w sécë.</p>
      
        <p><strong>Jesmë ôpen-zdrojowim projektã</strong>
            chtërnégò <a href="http://www.mozilla.org/projects">nôrzãdła</a> są ùżiwóné przë bùdowanim nôlepszich programów sécowëch.</p>
    </div>

    <p>Mómë wiarã w to że jakno nôwôżnészi rozwij społeczny ë technologiczny najich czasów, Internet je pùblëcznym dobrã chtërno mùszi bëc rozwijóny ë chroniony.</p>

    <p><a href="http://www.mozilla.org/foundation">Fùndacjô Mozilla</a> je niedochòdową òrganizacją wspiérającą ne céle. Ale nôwikszim atutã Mozillë są lëdze z całégò swiata chtërny nadżibają séc do swòjich pòtrzébnotów.
    <a href="http://www.mozilla.org/contribute">Të téż mòżesz pòmóc</a>!</p>
