    <div id="column-1">
        <h2>Dites-nous ce que vous pensez</h2>
        <p>En tant que bêta-testeur, vous faites partie de l'équipe développant Firefox&nbsp;4. Dites-nous quand vous aimez ou n'aimez pas quelque chose en cliquant sur le bouton <img src="<?=$screencap;?>" alt="Copie d'écran du bouton de retours d'utilisation" /><em>Réagir</em>. Il vous sera alors proposé de participer à des études anonymes d'usage du navigateur ou, ponctuellement, de répondre à une enquête.</p>
    </div>

    <div id="column-2">
        <h5>Comment fonctionnent les études d'usage&nbsp;?</h5>
        <p>Les études d'usage collectent des données statistiques anonymes sur votre utilisation de Firefox en tant que logiciel mais jamais sur ce que vous voyez ou faites sur le Web. Vous contrôlez l'ensemble du processus et aucune donnée n'est envoyée aux serveurs Test Pilot de Mozilla sans votre permission. Ces informations serviront à la conception de la version finale de Firefox&nbsp;4.</p>
    </div>

    <div id="column-3">
        <h5>Et si je ne veux pas participer&nbsp;?</h5>
        <p>Vous serez averti avant toute collecte de données d'usage ainsi qu'à chaque envoi de ces données à Mozilla. Vous pouvez choisir de ne pas participer à une étude particulière ou bien de désactiver toutes les études d'usage en cliquant sur le bouton <em>Réagir</em> afin d'accéder à vos préférences.</p>
    </div>

