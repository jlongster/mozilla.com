<div id="main-content">
  <h1 id="page-title">À propos de nous</h1>

<div class="intro">
  <p><strong>Nous sommes une communauté mondiale</strong>
    de <a href="../community">personnes travaillant ensemble</a> depuis 1998 pour façonner un Internet meilleur.</p>
  
  <p><strong>Nous sommes une organisation à but non lucratif</strong>
    qui se consacre à la promotion <a href="mission.html">de l'ouverture, de l'innovation et des possiblités</a> que peut offrir le Web.</p>
  
  <p><strong>Nous sommes un projet open source</strong>
    dont les <a href="../projects">logiciels</a> ont été utilisés pour créer quelques-unes des meilleures applications Internet.</p>
</div>

<p>Nous pensons qu'Internet, en tant qu'élément majeur du développement social et technologique de notre temps, doit être amélioré et protégé.</p>

<p>La <a href="/foundation">Fondation Mozilla</a> est une organisation à but non lucratif qui se consacre à ces objectifs, mais la vraie force derrière Mozilla, ce sont les personnes
du monde entier qui prennent part à la construction du Web qu'elles veulent. <a href="/contribute">Et vous pouvez aider</a>&nbsp;!</p>

</div>

