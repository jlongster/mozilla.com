        <h5>¿Cómo funcionan los estudios de usuarios?</h5>
        <p>Los estudios de usuarios obtienen estadísticas anónimas sobre cómo se usa Firefox, pero nunca sobre lo que estás viendo o haciendo. Tenés el control, y ninguna información se envía a los servidores de Test Pilot sin tu permiso. Estos datos serán usados como ayuda en el diseño de Firefox&nbsp;4.</p>
    </div>

    <div id="column-3">
        <h5>¿Qué pasa si no quiero participar?</h5>
        <p>Te avisaremos antes de que un estudio comience a obtener información, y nuevamente antes de que esa información sea enviada a Mozilla. Podés elegir no participar en cualquier estudio o descartar todos los estudios de usuario al hacer clic en el botón <em>Opinión</em> para administrar tu configuración.</p>
