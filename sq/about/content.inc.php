  <h1 id="page-title">Rreth Nesh</h1>

<div class="intro">
  <p><strong>Jemi një bashkësi mbarëbotërore</strong>
    of <a href="http://www.mozilla.org/community">personash që punojnë tok</a> që prej 1998 për të ndërtuar një Internet më të mirë.</p>
  
  <p><strong>Jemi një organizëm jofitimprurëse</strong>
    përkushtuar promuovimit të <a href="http://www.mozilla.org/about/mission.html">të qenit të hapur, risisë dhe mundësive</a> online.</p>
  
  <p><strong>Dhe jemi edhe një projekt me burim të hapët</strong>
    <a href="http://www.mozilla.org/projects">software</a>-et e të cilit janë përdorur për të ndërtuar disa nga zbatimet më të mira për Internet.</p>
</div>

<p>Besojmë se, si zhvillimi shoqëror dhe teknologjik më domethënës i kohës sonë, Interneti është një burim publik që duhet përmirësuar dhe mbrojtur.</p>

<p><a href="http://www.mozilla.org/foundation">Mozilla Foundation</a> është një organizëm jofitimprurës i përkushtuar këtyre synimeve, por forca e njëmendtë pas Mozilla-s janë personat anembanë botës që veprojnë për të ndërtuar një web si e dëshirojnë. <a href="http://www.mozilla.org/contribute">Dhe mund të nidhmoni dhe ju</a>!</p>

</div>

