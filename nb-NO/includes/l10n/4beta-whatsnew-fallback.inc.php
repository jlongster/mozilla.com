    <div id="column-2">
        <h5>Hvordan fungerer brukerundersøkelsene?</h5>
        <p>Brukerundersøkelsene samler inn anonym statistikk om hvordan du bruker Firefox, men aldri om hva du ser eller gjør på nettet. Du har full kontroll, og ingen data sendes til Mozilla Testpilot-tjenerne uten at du gir tillatelse til det. Informasjonen vil brukes for å hjelpe å utforme Firefox&nbsp;4.</p>
    </div>

    <div id="column-3">
        <h5>Hva om jeg ikke ønsker å delta?</h5>
        <p>Du vil få beskjed før en brukerundersøkelse starter å samle inn data, og dette er før noen informasjon sendes til Mozilla. Du kan velge å hoppe over en undersøkelse, eller du kan skru av alle brukerundersøkelser ved å trykke på <em>Tilbakemeldinger</em>-knappen for å bestemme innstillingene.</p>
    </div>

