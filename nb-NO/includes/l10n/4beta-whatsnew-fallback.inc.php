        <h2>Fortell hva du synes</h2>
        <p>Som en betatester er du del av teamet som lager Firefox&nbsp;4. Fortell oss hva du liker eller ikke liker ved å trykke på <img src="<?=$screencap;?>" alt="Tilbakemeldingsknapp-skjermbilde" /><em>Tilbakemelding</em>-knappen. Du vil også bli spurt om å delta i anonyme brukerundersøkelser, eller av og til delta i brukerundersøkelser.</p>
